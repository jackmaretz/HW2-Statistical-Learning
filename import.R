# setup of short time fourier transform -----------------------------------
fs = f1.dwn@samp.rate
winsize <- 2048
hopsize <- 512
nfft <- 2048
noverlap <- winsize - hopsize
#go
sp <-  specgram(x=f1.dwn@left,n=nfft,Fs=fs, window = winsize,overlap = noverlap)

#take a look
